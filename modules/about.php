
<h1>Tugas Pokok</h1>
Melaksanakan Pengelolaan Pertamanan dan Pemakaman<br>
<small class="form-text text-muted">
  Renstra 2013-2017
</small>
Fungsi :<br>

1. Penyusunan pelaksanaan rencana kerja dan anggaran Dinas Kehutanan;<br>
2. Perumusan kebijakan teknis pelaksanaan pengelolaan pertamanan dan pemakaman;<br>
3. Pembangunan taman, jalur hijau, pemakaman dan keindahan kota;<br>
4. Penataan, pemeliharaan dan perawatan taman, jalur hijau, keindahan  kota dan makam;<br>
5. Pengawasan dan pengendalian pemanfaatan makam, taman, jalur hijau, dan keindahan kota;<br>
6. Pelayanan, pembinaan dan pengendalian rekomendasi, perizinan, standarisasi dan / atau sertifikasi di bidang pertamanan dan pemakaman;<br>
7. Pengembangan peran serta masyarakat dibidang pertamanan dan pemakaman;<br>
8. Penyediaan tanah makam, pemetakan tanah makam, dan tata keindahan taman pemakaman umum;<br>
9. Pelayanan, perawatan/pengurusan, pengangkutan dan pemakaman jenazah termasuk jenazah orang terlantar;<br>
10. Penyelenggaraan penggalian dan atau pemindahan jenazah;<br>
11. Pemungutan, penatausahaan, penyetoran, dan pertanggungjawaban penerimaan retribusi pertamanan dan pemakaman;<br>
12. Penegakan peraturan perundang-undangan di bidang pertamanan dan pemakaman.<br>
13. Penyediaan, penatausahaan, penggunaan, pemeliharaan dan perawatan prasarana dan sarana pertamanan dan pemakaman;<br>
14. Pemberian dukungan teknis kepada masyarakatdan perangkat daerah;<br>
15. Pengelolaan kepegawaian, keuangan, barang, dan ketatausahaan Dinas Kehutanan;<br>
16. Pelaporan, dan pertanggungjawaban pelaksanaan tugas dan fungsi.<br>
